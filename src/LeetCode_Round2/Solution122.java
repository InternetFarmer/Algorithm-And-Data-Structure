package LeetCode_Round2;

/**
 *
 * @author Wu
 */
public class Solution122 {

    public int maxProfit(int[] prices) {
        int max = 0;
        int len = prices.length;
        for (int i = 1; i < len; i++) {
            if (prices[i] - prices[i - 1] > 0) {
                max += prices[i] - prices[i - 1];
            }
        }
        return max;
    }
}
